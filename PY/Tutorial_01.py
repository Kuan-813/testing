# TITLE: INTRODUCTION_WRITE PYTHON CODE
name = "Kuannnn"
age = 34
weight = 52.5
is_female = True

print(name)                         # Output: Kuannnn
print(age)                          # Output: 34    
print(weight)                       # Output: 52.5
print(is_female)                    # Output: True
