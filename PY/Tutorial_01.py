# TITLE: INTRODUCTION_MATH OPERATION
x = 10
y = 3

print(x + y)                        # [add]             # Output: 13
print (x - y)                       # [subtract]        # Output: 7
print ( x * y)                      # [multiply]        # Output: 30
print (x / y)                       # [divide]          # Output: 3.3333333333333335
print (x // y )                     # [floor divide]    # Output: 3
print (x % y)                       # [modulo]          # Output: 1
print (x ** y)                      # [power]           # Output: 1000



